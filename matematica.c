#include "matematica.h"
#include <math.h>


float calcular_seno(float angulo_radianos) //Função calculo de seno pela math.h.
{
    return sin(angulo_radianos);
}

float calcular_cosseno(float angulo_radianos) //Função calculo de cosseno pela math.h.
{
    return cos(angulo_radianos);
}

float calcular_tangente(float angulo_radianos) //Função calculo de tangente pela math.h.
{
    return tan(angulo_radianos);
}
